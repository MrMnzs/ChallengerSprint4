package br.com.fiap.model;

public class Resposta {
	private int id;
	private int nrResposta;
	private String dsResposta;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNrResposta() {
		return nrResposta;
	}
	public void setNrResposta(int nrResposta) {
		this.nrResposta = nrResposta;
	}
	public String getDsResposta() {
		return dsResposta;
	}
	public void setDsResposta(String dsResposta) {
		this.dsResposta = dsResposta;
	}
}